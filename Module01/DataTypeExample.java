public class DataTypeExample {
    public static void main(String[] args) {
        int a = 16;
        double b = 4.0;
        boolean c = false; // Note: all lowercase "false" and "true"

        double x = a / b;
        double y = (int) (a / b);  // Typecasting

        String hello = "Hello!";
        char h = 'h';  // Note: char type values use single quotation marks

        int unused;
    }
}